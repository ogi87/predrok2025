    private void popuniKomboPolja() throws Exception{
        // 1. popunjavanje predmeta
        List<Predmet> predmeti = Controller.getInstance().ucitajSvePredmete();
        cmbPredmet.setModel(new DefaultComboBoxModel(predmeti.toArray()));
        
        List<OblikNastave> oblici = Controller.getInstance().ucitajSveObliciNastave();
        cmbOblikNastave.setModel(new DefaultComboBoxModel(oblici.toArray()));
        
        cmbPredmet.setSelectedIndex(-1);
        cmbOblikNastave.setSelectedIndex(-1);
        
    }

