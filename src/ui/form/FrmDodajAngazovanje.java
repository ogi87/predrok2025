    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        Predmet izabraniPredmet = (Predmet) cmbPredmet.getSelectedItem();
        OblikNastave izabraniOblik = (OblikNastave) cmbOblikNastave.getSelectedItem();
        
        if(izabraniPredmet == null || izabraniOblik == null){
            JOptionPane.showMessageDialog(this, "Morate izabrati i predmet i oblik nastave", "Upozerenja", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            Controller.getInstance().sacuvajAngazovanje(ulogovaniNastavnik, izabraniPredmet, izabraniOblik);
            JOptionPane.showMessageDialog(this, "Angazovanje je uspesno sacuvano","Uspeh", JOptionPane.INFORMATION_MESSAGE);
            
            if(formaZaOsvezavanje != null){
                formaZaOsvezavanje.osveziTabelu();
            }
            
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska pri snimanju angazovanje: " + e.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);            
        }
        
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

